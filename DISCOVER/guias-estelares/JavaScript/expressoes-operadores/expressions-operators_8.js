// Operadores de comparação
// Irá comparar não apenas os valores e retornar um Boolean como resposta à comparação.

let one = 1
let two = 2
// > Maior que
console.log(one > two)
// >= Maior igual a 
console.log(one >= 1)
console.log(two >= 1)
// < Menor que
console.log(one < two)
// Menor igual a
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)
